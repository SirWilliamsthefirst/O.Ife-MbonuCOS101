fn main() {
    // addition
    let sum:u64 = 5550 + 7310;
    println!("the sum of 5550 and 7310 = {}",sum);

    //subtraction
    let difference:f64 = 95.5 - 4.3;
    println!("The difference of 95.5 and 4.3 = {}",difference);

    //multiplication
    let multiplication:u64 = 4 * 30; 
    println!("the multiplication of 4 and 30 = {}",multiplication);

    // divison
    let division:f64 = 56.7 / 32.2;
    println!("the division of 56.7 and 32.2 = {}",division);

    //remainder
    let remainder = 43 % 5;
    println!("the remainder of 43 and 5 = {}",remainder);
}
